    void CargarJTabla(){
        
        ListadoProductos = new ArrayList<Producto>();
        
        Object [] fila;
        
        for (Producto oProducto : ListadoProductos){
            
            fila = new Object [4];
            fila[0] = oProducto.getNombre();
            fila[1] = oProducto.getCantidad();
            fila[2] = oProducto.getCosto();
            fila[3] = oProducto.getAlmacen();
            modeloTablaProductos.addRow(fila);
            
        }
        
        
       
        
        
    }
    
       
       
    void InicializarModeloTabla(){
        
        modeloTablaProductos = new DefaultTableModel();
        modeloTablaProductos.addColumn("Nombre");
        modeloTablaProductos.addColumn("Cantidad");
        modeloTablaProductos.addColumn("Costo");
        modeloTablaProductos.addColumn("Almacen");
        jtblproductos.setModel(modeloTablaProductos);
        
    }
    
    void RegistrarProducto(){
        
        Producto oProducto = new Producto();
        oProducto.setNombre(txtnombre.getText());
        oProducto.setCantidad(txtcantidad.getText());
        oProducto.setCosto(txtcosto.getText());
        char Almacen = cmbalmacen.getSelectedIndex() == 0 ? 'P' : 'H';
        oProducto.setAlmacen(Almacen); 
        ListadoProductos.add(oProducto);
        
    }
    
    void EliminarProducto(){
        
        int filaSeleccionada = jtblproductos.getSelectedRow();
        
        //casteo / casting - conversion de un tipo a otro tipo
        String nombre = (String)modeloTablaProductos.getValueAt(filaSeleccionada, 0);
        
        int posicion = -1;
        
        for (int i = 0; i < ListadoProductos.size(); i++) {
            
            if (ListadoProductos.get(i).getNombre().equals(nombre)){
                posicion = i;
                break;
            }
                
        }
        
        if (posicion >= 0){
            Producto aux = ListadoProductos.get(posicion);            
            ListadoProductos.remove(aux);           
            
        }
    }
    
    
    
